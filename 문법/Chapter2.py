# Chapter2. 변수와 데이터 타입
# 변수
# 어떤 값을 저장하기위해 사용되는 값
name = "seungho"
age = 22
print(name, age)
# 데이터 타입
# 데이터의 종류를 나타내는 방식
# int 정수 타입 ex) -1,0,22
# float 실수 타입 ex) 3.14,1.333333
# str 문자열 타입 ex) seungho, python
# bool 논리 타입(참과 거짓) ex) True,False
#
# type 함수 : 데이터 타입을 알려주는 함수
print(name, type(name))
print(age, type(age))
weight = 55.8
isPythonFun = True
print("weight : ", weight, "type : ", type(weight))
print("isPythonFun? : ", isPythonFun, "type : ", type(isPythonFun))
